// ============================================================
// 模式 → Prompt 映射
// ============================================================

export function getModePrompt(mode: string): string {
  switch (mode) {
    case "experiment":
      return EXPERIMENT_PROMPT;
    case "review":
      return REVIEW_PROMPT;
    case "coding":
      return CODING_PROMPT;
    default:
      return GLOBAL_SYSTEM_PROMPT;
  }
}

// ============================================================
// 联网搜索时获取对应全局提示词
// ============================================================

export function getGlobalPrompt(webSearchActive: boolean): string {
  return webSearchActive ? GLOBAL_SYSTEM_PROMPT_WEB_SEARCH : GLOBAL_SYSTEM_PROMPT;
}
