export function getModePrompt(mode: string): string {
  switch (mode) {
    case "experiment":
      return EXPERIMENT_PROMPT;
    case "review":
      return REVIEW_PROMPT;
    case "coding":
      return CODING_PROMPT;
    default:
      return GLOBAL_SYSTEM_PROMPT;
  }
}

